package com.basics;

public class EscapeSequences {
    public static void main(String[] args) {

        //Escaping quotes
        System.out.println("\"Hello Dear User.\"");

        //Escaping backslash
        System.out.println("c:\\Program Files\\git");

        //New line and tab
        System.out.println("The value of A :55\t and \n B :33 ");
    }
}
